"$TEMPO_HOME/tempo" "$INPUT_FILE" "$ASM_FILE"
if [ $? -ne 0 ]; then
    echo "âŒ Compilation failed"
    exit 1
fi

# Step 2: Assemble  
echo "  [2/3] Assembling..."
as -arch x86_64 "$ASM_FILE" -o "$OBJ_FILE"
if [ $? -ne 0 ]; then
    echo "âŒ Assembly failed"
    exit 1
fi

# Step 3: Link
echo "  [3/3] Linking..."
ld -o "$OUTPUT_NAME" "$OBJ_FILE" -lSystem -L/usr/lib
if [ $? -ne 0 ]; then
    echo "âŒ Linking failed"
    exit 1
fi

echo "âœ… Build successful: $OUTPUT_NAME"
echo "ðŸš€ Run with: ./$OUTPUT_NAME"

# Cleanup intermediate files
rm -f "$ASM_FILE" "$OBJ_FILE"
EOF

